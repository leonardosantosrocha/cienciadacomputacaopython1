# Por que testar
# - Seres humanos erram
# - Programas contém erros (bugs)
#   - Sintaxe
#   - Lógica
# - Software robusto deve conter no mínimo possível de erros
# - Bugs podem causar catástrofes
# - Bugs podem causar desconforto
#   - Fazer com o que seu cliente abandone o produto
# 
# 
# Como testar
# - Testes manuais
#   - Cansativo
#   - Você vai testar apenas alguns casos
#   - Você vai testar apenas algumas vezes
#   - Não é inteligente
# - Testes automatizados
#   - Bateria de testes que cobre o máximo possível do seu código
#   - Executada rotineiramente varias vezes por dia
#   - Inteligente
#
#
# Arcabouços de Testes Automatizados
# - Vamos aprender o pytest
# - Considera que arquivos do tipo test_*.py e *_test.py são testes
# - Executa funções e métodos do tipo test_*
# - Considera classes do tipo Test*
# -
# -
# -
# -
# Como utilizar Pytest
# - Vá até o terminal (cmd) do windows ou vscode
# - Digite py.test nome_do_arquivo
