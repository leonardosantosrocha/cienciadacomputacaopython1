# Booleanos são valores que assumem verdadeiro ou falso
# True or False | 0 or 1

# True
print(1 > 2)

# False
print(2 < 1)

# And
print(1 < 2 and 2 < 3) # Todas as condições precisam ser verdadeiras

# Or
print(1 < 2 or 2 > 3) # Apenas uma condição precisa ser verdadeira

# Not (inversor)
print(not True)

# Not (inversor)
print(not False)

# Precedência de operadores
# 7 (alto) - exponenciação - **
# 6 - multiplicação - *, /, //, %
# 5 - adição - +, -
# 4 - relacional - ==, !=, <=, >=, <, >
# 3 - lógico - not
# 2 - lógico - and
# 1 (baixo) - or