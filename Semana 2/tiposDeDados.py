# Inteiros
type(10)

# String
type('Olá, Leonardo!')

# Float
type(5/2)

# Divisão inteira
10 // 3

# Resto da divisão
10 % 3

# Guardando valores em variáveis
peso = 80
altura = 1.65
imc = peso / (altura ** 2)
print(imc)

# Convertendo float em inteiro
imcInteiro = int(imc)
print(imcInteiro)

# Comprimento do texto com len()
texto = 'Bom dia, Leonardo!'
comprimento = len(texto)
print(comprimento)